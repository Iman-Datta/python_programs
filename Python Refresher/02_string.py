name = "Iman Datta"

print(name.upper())      # Converts all characters in the string to uppercase

print(name.lower())      # Converts all characters in the string to lowercase

print(name.count("a"))   # Counts how many times the character 'a' appears in the string

print(len(name))         # Returns the total number of characters in the string (including spaces)

print(name[0:3])         # Extracts characters from index 0 up to (but not including) index 3

print(name[1:-1])        # Extracts characters starting from index 1 up to the second-last character

print(name[1:9])         # Extracts characters from index 1 up to (but not including) index 9

print(name[-9:-1])       # Extracts characters from index -9 up to (but not including) index -1